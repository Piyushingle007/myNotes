<script lang="ts">
  import { appState } from '../stores/appState.svelte';

  let inputText = $state<string>('');
  let outputText = $state<string>('');
  let selectedTool = $state<'summarize' | 'rewrite' | 'table' | 'grammar'>('summarize');
  let selectedNotePath = $state<string>('');
  let isProcessing = $state<boolean>(false);
  let rewriteTone = $state<'professional' | 'casual' | 'academic' | 'concise'>('professional');

  // Load text from selected note
  function handleLoadNote() {
    const note = appState.notes.find(n => n.path === selectedNotePath);
    if (note) {
      inputText = note.content;
    }
  }

  // AI Mock Engine
  function processAiAction() {
    if (!inputText.trim()) {
      alert('Please enter some text or load a note first.');
      return;
    }

    isProcessing = true;
    outputText = '';

    setTimeout(() => {
      isProcessing = false;
      const text = inputText.trim();

      if (selectedTool === 'summarize') {
        // Simple sentence extractor summary mock
        const sentences = text.split(/[.!?]+/).map(s => s.trim()).filter(s => s.length > 10);
        if (sentences.length === 0) {
          outputText = `### Key Takeaways\n- Focus is on capturing thoughts clearly.\n- Add more text for a deeper summary.`;
          return;
        }
        const keyBullets = sentences.slice(0, Math.min(sentences.length, 4));
        outputText = `### Key Summary Bullet Points 📋\n\n` + 
          keyBullets.map(s => `- **Key Concept:** ${s}.`).join('\n') + 
          `\n\n*Generated by myNotes AI Summarizer*`;

      } else if (selectedTool === 'rewrite') {
        // Rewriter mock
        if (rewriteTone === 'professional') {
          outputText = `### Professional Revision 👔\n\n` + 
            `We have polished the text to establish a highly professional, business-centric vocabulary and optimized flow:\n\n` +
            text.replace(/\b(i want to|i need to)\b/gi, 'It is imperative that we')
                .replace(/\b(good|nice|cool)\b/gi, 'exceptional')
                .replace(/\b(do this|do that)\b/gi, 'execute these processes') +
            `\n\n*Generated by myNotes AI Professional Editor*`;
        } else if (rewriteTone === 'casual') {
          outputText = `### Casual Tone Revision 👋\n\n` +
            `Here is a relaxed and friendly rewrite perfect for conversational shares:\n\n` +
            `Hey! So here's the deal: ` + text.replace(/\b(therefore|consequently)\b/gi, 'so basically')
                                              .replace(/\b(implement|execute)\b/gi, 'set up')
                                              .replace(/\b(prior to)\b/gi, 'before') + 
            ` 🙌\n\n*Generated by myNotes AI Casual Editor*`;
        } else if (rewriteTone === 'academic') {
          outputText = `### Academic Analysis Revision 🎓\n\n` +
            `The narrative has been refined to utilize rigorous scholarly syntax and formal analytical structure:\n\n` +
            `A systematic review of the input parameters indicates: ` + 
            text.replace(/\b(I think)\b/gi, 'It is hypothesized')
                .replace(/\b(very|extremely)\b/gi, 'significantly')
                .replace(/\b(lots of|a lot of)\b/gi, 'a substantial multitude of') +
            `\n\n*Generated by myNotes AI Academic Editor*`;
        } else {
          // Concise
          const short = text.substring(0, Math.min(text.length, 180)) + (text.length > 180 ? '...' : '');
          outputText = `### Concise Revision ⚡\n\n` +
            `Refined and condensed version focusing purely on essential facts:\n\n` +
            `*"${short}"*\n\n*Generated by myNotes AI Concise Editor*`;
        }

      } else if (selectedTool === 'table') {
        // Table Generator
        const lines = text.split('\n').map(l => l.trim()).filter(Boolean);
        if (lines.length === 0) {
          outputText = `| Header 1 | Header 2 |\n| --- | --- |\n| Row 1 Col 1 | Row 1 Col 2 |`;
          return;
        }

        // Try to parse CSV or space-separated lists
        let markdownTable = '';
        const parsedRows = lines.map(line => {
          if (line.includes(',')) {
            return line.split(',').map(cell => cell.trim());
          } else if (line.includes('\t')) {
            return line.split('\t').map(cell => cell.trim());
          } else {
            return line.split(/\s{2,}/).map(cell => cell.trim());
          }
        });

        const maxCols = Math.max(...parsedRows.map(r => r.length));
        if (maxCols <= 1) {
          // Fallback simple list to table
          markdownTable = `| Item Index | Content Descriptor |\n| --- | --- |\n`;
          parsedRows.forEach((r, idx) => {
            markdownTable += `| #${idx + 1} | ${r[0] || ''} |\n`;
          });
        } else {
          // Build proper header
          const headers = parsedRows[0];
          while (headers.length < maxCols) {
            headers.push(`Header ${headers.length + 1}`);
          }
          markdownTable += `| ${headers.join(' | ')} |\n`;
          markdownTable += `| ${headers.map(() => '---').join(' | ')} |\n`;
          
          // Build rows
          parsedRows.slice(1).forEach(row => {
            while (row.length < maxCols) {
              row.push('');
            }
            markdownTable += `| ${row.join(' | ')} |\n`;
          });
        }
        outputText = `### Generated Markdown Table 📊\n\n${markdownTable}\n\n*Generated by myNotes AI Table Converter*`;

      } else if (selectedTool === 'grammar') {
        // Grammar Checker mock
        let corrected = text;
        const corrections: string[] = [];

        // Simple mock substitutions for spelling & grammar check
        const rules = [
          { regex: /\bteh\b/g, replacement: 'the', msg: 'Spelling error: "teh" -> "the"' },
          { regex: /\brecieve\b/g, replacement: 'receive', msg: 'Spelling error: "recieve" -> "receive"' },
          { regex: /\bdont\b/g, replacement: "don't", msg: 'Punctuation: "dont" -> "don\'t"' },
          { regex: /\bseperate\b/g, replacement: 'separate', msg: 'Spelling error: "seperate" -> "separate"' },
          { regex: /\bi\b/g, replacement: 'I', msg: 'Capitalization: "i" -> "I"' }
        ];

        rules.forEach(rule => {
          if (rule.regex.test(corrected)) {
            corrected = corrected.replace(rule.regex, rule.replacement);
            corrections.push(rule.msg);
          }
        });

        if (corrections.length === 0) {
          outputText = `### Grammar and Style Check Clean! ✨\n\nNo glaring spelling or grammar issues were identified. Excellent writing!\n\n---\n\n${text}`;
        } else {
          outputText = `### Grammar Corrections Applied 📝\n\n` +
            `We identified and fixed ${corrections.length} style issues:\n` +
            corrections.map(c => `- *${c}*`).join('\n') +
            `\n\n#### Corrected Document Text:\n\n${corrected}`;
        }
      }
    }, 1200);
  }

  async function handleCreateNote() {
    if (!outputText) return;
    const name = prompt('Enter name for the AI note:', `${selectedTool.toUpperCase()} Output`);
    if (name !== null) {
      await appState.createNote(name);
      appState.activeNoteContent = outputText;
      await appState.saveActiveNote(true);
      appState.activeTab = 'documents';
    }
  }

  function handleCopyToClipboard() {
    if (!outputText) return;
    navigator.clipboard.writeText(outputText);
    alert('AI text copied to clipboard!');
  }
</script>

<div class="ai-container">
