          </button>
          <button 
            class="chip-tool" 
            class:active={selectedTool === 'table'}
            onclick={() => selectedTool = 'table'}
          >
            📊 CSV-to-Table
          </button>
          <button 
            class="chip-tool" 
            class:active={selectedTool === 'grammar'}
            onclick={() => selectedTool = 'grammar'}
          >
            ✅ Spell Auditor
          </button>
        </div>

        <!-- Tool specific parameters -->
        {#if selectedTool === 'rewrite'}
          <div class="tool-params">
            <span class="md3-label-medium">Rewrite Tone:</span>
            <div class="tone-options">
              {#each ['professional', 'casual', 'academic', 'concise'] as tone}
                <button 
                  class="tone-btn" 
                  class:active={rewriteTone === tone}
                  onclick={() => rewriteTone = tone as any}
                >
                  {tone}
                </button>
              {/each}
            </div>
          </div>
        {/if}
      </div>

      <button class="md3-btn process-btn" onclick={processAiAction} disabled={isProcessing}>
        {#if isProcessing}
          <div class="spinner"></div> Generating...
        {:else}
          ✨ Execute AI Action
        {/if}
      </button>
    </div>

    <!-- Right Panel: Output -->
    <div class="panel output-panel md3-card-outlined">
      <div class="output-header">
        <span class="md3-label-large">AI Output Canvas</span>
        {#if outputText}
          <div class="output-actions">
            <button class="action-icon-btn" onclick={handleCopyToClipboard} title="Copy to clipboard">📋</button>
            <button class="action-icon-btn" onclick={handleCreateNote} title="Save as note">💾</button>
          </div>
        {/if}
      </div>

      {#if isProcessing}
        <div class="output-loading">
          <div class="pulse-loader"></div>
          <p>Processing text parameters and crafting layout...</p>
        </div>
      {:else if outputText}
        <div class="output-content">
          <pre>{outputText}</pre>
        </div>
      {:else}
        <div class="output-empty">
          <div class="magic-icon">🔮</div>
          <p>AI results will appear here after executing an action on the left.</p>
        </div>
      {/if}
    </div>
  </div>
</div>

