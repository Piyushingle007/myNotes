        <select id="note-select" class="note-dropdown" bind:value={selectedNotePath} onchange={handleLoadNote}>
          <option value="">-- Choose a Note --</option>
          {#each appState.notes as note}
            <option value={note.path}>{note.path}</option>
          {/each}
        </select>
      </div>

      <!-- Text Input area -->
      <textarea 
        class="input-textarea" 
        placeholder="Type, paste content, or load a note to start transforming..."
